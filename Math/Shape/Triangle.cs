namespace Math.Shape
{
    public class Triangle
    {
        public float sideSizeA;
        public float sideSizeB;
        public float sideSizeC;

        public Triangle(float sideSizeA, float sideSizeB, float sideSizeC)
        {
            this.sideSizeA = sideSizeA;
            this.sideSizeB = sideSizeB;
            this.sideSizeC = sideSizeC;
        }
    }
}
